//Задача: Прыжки по буквам
/*
Дана цепочка из N символов, состоящая из прописных латинских букв. 
Необходимо пройти с первого символа цепочки до последнего символа, прыгая не более чем на K символов. 
Стоимость прыжка, при котором символ не меняется, равна 0, а стоимость прыжка на другой символ равна 1.

Напишите программу, вычисляющую наименьшую стоимость перехода с первого на последний символ.

В первой строке ввода содержатся два целых числа – длина цепочки N (1 <= N <= 1000000) и максимальная длина прыжка K (1 <= K <= N).
В второй строке содержится цепочка из N прописных латинских букв.

Вывести одно целое число – минимальную стоимость перехода.

Пример ввода
10 2
ABABBCACBC

A BABBCACBC
0 1121
Вывод для примера
2
*/
#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;

int min_cost(string s, int K) {
	int N = s.size();
	vector<int> dp(N, 0);

	for (int i = 1; i < N; ++i) {
		dp[i] = dp[i - 1] + 1;
		for (int j = i - 2; j >= max(0, i - K); --j) {
			if (s[j] == s[i]) {
				dp[i] = min(dp[i], dp[j] + 1);
			}
		}
	}

	return dp[N - 1];
}

int main() {
	int N, K;
	string s;

	// Ввод данных
	cin >> N >> K;
	cin >> s;

	// Вычисление и вывод результата
	cout << min_cost(s, K) << endl;

	return 0;
}


