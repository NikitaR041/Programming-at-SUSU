# Вводятся номера телефонов в формате: +7xxxxxxxxxx +6xxxxxxxxxx  +7xxxxxxxxxx +5xxxxxxxxxx … пока не будет введена пустая строка. 
# Здесь x – это цифры. Необходимо сформировать словарь, в котором  ключами будут коды стран: «+7», «+5», «+6», «+8», …, а значениями – 
# список соответствующих номеров телефонов.


dict_sample = {}
mass = []
x = input("Введите свой номер телефона")
while x != "":
    mass.append(x)
    x = input("Введите свой номер телефона")

for x in mass:
    #Если нет такого ключа, то добавляем ключ и значение
    if(x[:2] not in dict_sample):
        dict_sample[x[:2]] = []
    #Иначе добавляем существует такой ключ и добавляем только значение
    dict_sample[x[:2]].append(x[2:]) #Грубо говоря мы обратились к ключу x[:2] и хотим добавить значение-список - свои элементы .append(x[2:])

print(dict_sample)
