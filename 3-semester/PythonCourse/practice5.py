# #элементов в последовательности.

def fibonacci(n):
    if n <= 0:
        return "ERROR!"
    elif n == 1:
        return 0
    elif n == 2:
        return 1

    a, b = 0, 1
    for _ in range(2, n):
        a, b = b, a + b 
    return b
# Запрос количества элементов у пользователя
num_elements = int(input("Введите количество элементов в последовательности Фибоначчи: "))
