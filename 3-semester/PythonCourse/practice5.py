def comparing_digit(num1, num2):
   m = list()
   if num1 <= num2:
       while num1 <= num2:
           m.append(num1)
           num1 += 1
   else:
       while num1 >= num2:
           m.append(num1)
           num1 -= 1
   print(f"Вот ваш список цифр: {m}")
   
