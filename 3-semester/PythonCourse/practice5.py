import math

def calc_grad(x ,y, z):
   #Вычислим изначально букву d
   d = (x**2 + y**2 + z**2) / (2*x*y)
   #Проверим область
   if d < -1 or d > 1:
       return "Ошибка области определения"
   rad = math.acos(d) #Получаем радианы
   deg = math.degrees(rad) #Переводим в градусы

   return deg

x = int(input("Введите число x: "))
y = int(input("Введите число y: "))
z = int(input("Введите число z: "))
